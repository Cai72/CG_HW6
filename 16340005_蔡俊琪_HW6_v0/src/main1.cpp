        Shader lightingShader(vs.c_str(), fs.c_str());
        Shader lampShader("2.2.lamp.vs", "2.2.lamp.fs");
        lightingShader.use();
        lightingShader.setVec3("objectColor", 1.0f, 0.5f, 0.31f);
